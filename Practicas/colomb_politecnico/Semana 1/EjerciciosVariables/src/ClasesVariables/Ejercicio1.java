package ClasesVariables;

public class Ejercicio1 {
    public static void main(String[] args) {
        String nombre = "Nicolas";
        float x = 25.15f;
        final double valorDolar = 959D;
        float sieteDecimalesPi = 3.1415926f;
        double quinceDecimalesE = 2.718281828459045;

        System.out.println("Valor variable nombre: " + nombre);
        System.out.println("Valor variable x: " + x);
        System.out.println("Valor variable valorDolar: " + valorDolar);
        System.out.println("Valor variable sieteDecimalesPi: " + sieteDecimalesPi);
        System.out.println("Valor variable quinceDecimalesE: " + quinceDecimalesE);
    }
}
